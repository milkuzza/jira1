export type Translations = typeof translations.en;

// Needed to satisfy TypeScript when indexing with a runtime Locale value.
type TranslationMap = { [K in Locale]: Translations };

export function useT(): Translations {
  const locale = useLocaleStore((s) => s.locale);
  return (translations as unknown as TranslationMap)[locale];
}
